                                    <h1 class="post-title">What does a Construction Worker do?</h1>
                                    <div class="post-details"> <span class="post-date"><i class="icon-clock"></i> July 25, 2015</span> <span class="post-author"><i class="icon-head"></i>
                                <a href="#">christos</a></span> <span class="post-category"><i class="icon-clipboard"></i>

                                <a href="#">No comments</a></span> </div>
                                </header>
                                <div class="post-body">
                                    <p>Construction workers work on all construction sites, doing a wide range of tasks from the very easy to the extremely difficult and hazardous.</p>
                                    <p>Although many of the tasks they do require some training and experience, most jobs usually require little skill and can be learned quickly.A construction worker typically does the following:</p>
                                    <ul>
                                        <li>Cleans and prepares construction sites by removing debris and possible hazards</li>
                                        <li>Loads or unloads building materials to be used in construction</li>
                                        <li>Builds or takes apart bracing, barricades, forms (moulds that determine the shape of concrete), scaffolding, and temporary structures</li>
                                        <li>Digs trenches, backfills holes, or compacts earth to prepare for construction</li>
                                        <li>Operates or tends equipment and machines used in construction, such as concrete mixers</li>
                                        <li>Helps other craft workers with their duties</li>
                                        <li>Follows construction plans and instructions from the people they are working for</li>
                                    </ul>
                                    <p>Construction workers do a variety of construction-related activities during all phases of construction. Although most are generalists—such as those who install barricades, cones, and markers to control traffic patterns—many
                                        others specialize. For example, those who operate the machines and equipment that lay concrete or asphalt on roads are more likely to specialize in those areas.</p>
                                    <p>The following are occupational specialties::</p>
                                    <ul>
                                        <li>Building homes and businesses</li>
                                        <li>Tearing down buildings</li>
                                        <li>Removing hazardous materials</li>
                                        <li>Building highways and roads</li>
                                        <li>Digging tunnels and mine shafts</li>
                                    </ul>
